<script setup lang="ts">
const { locale } = useI18n()
const { passengerAndroidLink, driverAndroidLink } = useUserType()

const handleAppClick = (appType: 'passenger' | 'driver') => {
  const isIOS = /iPad|iPhone|iPod/.test(navigator.userAgent) || (navigator.platform === 'MacIntel' && navigator.maxTouchPoints > 1)

  if (isIOS) {
    alert(appType === 'passenger' ? 'iOS Yolcu uygulaması yakında!' : 'iOS Sürücü uygulaması yakında!')
    return
  }

  // If not iOS, redirect to Android link
  const link = appType === 'passenger' ? passengerAndroidLink : driverAndroidLink
  if (link) {
    window.open(link, '_blank')
  }
}

// AIO Freshness Signal: Generate human-readable date for AI crawlers
const lastUpdated = computed(() => {
  return new Date().toLocaleDateString(locale.value, {
    day: 'numeric',
    month: 'long',
    year: 'numeric'
  })
})
</script>
